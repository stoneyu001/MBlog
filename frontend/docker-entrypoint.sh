#!/bin/sh
# ============================================
# Frontend 容器启动脚本
# 功能：确保首次启动时有预构建的静态文件
# ============================================

# 强制从预构建目录同步文件到挂载卷
# 这确保了镜像中的最新构建（包含 CSS/JS）总是被使用
echo "正在从预构建目录同步文件..."
cp -rf /app/dist-backup/* /usr/share/nginx/html/
echo "文件同步完成"

# 启动 Nginx
exec nginx -g "daemon off;"
